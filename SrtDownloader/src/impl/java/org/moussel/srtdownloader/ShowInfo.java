package org.moussel.srtdownloader;

public class ShowInfo {

	String name;
	
	public ShowInfo() {
	}
	
	public ShowInfo(String name) {
		this.name = name;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
}
